// ── Cross-enricher smoke test ─────────────────────────────────────────────────

/// All three enrichment passes must accept an empty symbol slice and return 0
/// without panicking, regardless of what is or is not installed.
#[test]
fn all_enrichers_accept_empty_symbol_slice() {
    let dir = tempfile::tempdir().unwrap();
    let db_path = dir.path().join("index.db");
    let db = Database::open(&db_path).expect("db open");

    // Provide the minimal workspace files so we get past gate 1 for each pass.
    std::fs::write(dir.path().join("tsconfig.json"), "{}").unwrap();
    std::fs::write(dir.path().join("Cargo.toml"), "[package]\nname=\"x\"\n").unwrap();

    assert_eq!(run_ts_enrichment(dir.path(), &mut [], &db), 0);
    assert_eq!(run_rustdoc_enrichment(dir.path(), &mut [], &db), 0);
    assert_eq!(run_pyright_enrichment(dir.path(), &mut [], &db), 0);
}
